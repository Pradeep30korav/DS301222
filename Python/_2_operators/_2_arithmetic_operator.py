no1 = 10
no2 = 5

add = no1 + no2 
print("Addition : ",add)

sub = no1 - no2
print("Subtraction : ",sub)

# Note: Division operator always returns value in float type
div = no1 / no2 
print("Division : ",div)

mul = no1 * no2
print("Multiplication : ",mul)

# Modulus returns remainder
mod = no1 % no2
print("Modulus : ",mod)

floor_div = no1 // no2
print("Floor Division : ",floor_div)

expo = no1 ** no2
print("Exponentia : ",expo)

