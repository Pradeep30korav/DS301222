# Membership Operator
# in      - returns True, if object is present in the sequence 
# not in  - returns False, if object is present in the sequence + returns True, if object is not present in the sequence


lst = [7,8,1,9,4,5]
in_demo = 1 in lst
print("In demo : ",in_demo)

not_in_demo = 1 not in lst
print("Not in demo : ",not_in_demo)